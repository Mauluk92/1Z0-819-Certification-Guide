public class WrapperClasses {
    public static void main(String[] args){

        // Initializing different wrapper classes

        Byte byteWrapper = Byte.valueOf((byte) 1);
        Short shortWrapper = Short.valueOf((short) 1);
        Integer integerWrapper = Integer.valueOf(1);
        Long longWrapper = Long.valueOf(1L);

        Float floatWrapper = Float.valueOf(1F);
        Double doubleWrapper = Double.valueOf(1.0);

        Boolean booleanWrapper = Boolean.valueOf(true);

        Character charWrapper = Character.valueOf('b');

    }
}