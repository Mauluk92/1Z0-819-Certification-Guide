public class ModulusOperator {
    public static void main(String[] args){
        int value = 10;
        int firstDivisor = 3;
        int secondDivisor = 2;

        System.out.println("The remainder of 10/3 is " + (value%firstDivisor));
        System.out.println("The remainder of 10/2 is " + (value%secondDivisor));

    }
}