public class NegationOperator {
    public static void main(String[] args){
        int value = 4;
        System.out.println("The value of the numeric expression is " + 1);
        System.out.println("Applying the negation operator (-) to the value produces " + -1);
        System.out.println("Applying twice the negation operator (-(-)) to the value produces " + -(-1));
    }
}