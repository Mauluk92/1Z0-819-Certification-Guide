public class LiteralPrimitiveDataType {
    public static void main(String[] args){
        int intLiteral = 10; // Doesn't need any particular literal
        long longLiteral = 10L; // Without the L won't compile. It is also possible to use an l lowercase
        float floatLiteral = 10.0f; // Without an f it won't compile because it will be a double
        double doubleLiteral = 10.0; // Doesn't require any particular literal
    }
}